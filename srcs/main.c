#include "./../includes/ft.h"

int		main(int ac, char **av)
{
	int	a;

	a = 1;
	if (ac >= 2)
	{
		while (a < ac)
		{
			ft_error(av[a], ft_cat(av[a]));
			a++;
		}
		return (0);
	}
	else
		return (1);
}
